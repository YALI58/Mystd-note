# 未在函数内部定义n1,那么默认使用全局变量n1

n1=100

def f1():
    print(n1)

f1()#100

# 在函数内部重新定义了n2,那么根据就近原则,使用的就是函数内部重新定义的n2

n2 = 100


def f2():
    n2 = 200
    print(n2)


f2()  # 200

# global 关键字标明使用全局变量
n3 = 100
def f3():
    global n3
    n3=200
    print(f"n3->{n3}")
f3()
print(n3) 