# 算数运算符

# 对于除号/,结果返回是小数(float)

print(3 / 1)  # 3.0

# 对于取整除//,返回商的整数部分(并且是向下取整)
print(10 // 3)  # 3
print(-9 // 2)  # -5

# 当对一个数取模时,对应的运算公式: a%b = a-a//b*b
print(10 % 3)  # 10 % 3 = 10 -10// 3 *3 = 10 -3 *3 =1w

# *号另外使用方法
print("*" * 30)  # ****************************** 