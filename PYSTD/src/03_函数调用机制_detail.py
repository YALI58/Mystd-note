# 函数调用机制
# 1. 当函数被调用时，会开辟一个独立的栈空间
# 2. 形参接收实参，把实参的值赋给形参
# 3. 执行函数体的代码
# 4. 如果有返回值，将返回值返回给调用者
# 5. 函数执行完毕后，栈空间被释放

def test(n):
    print(f"n={n}")  # n是形参，接收实参的值
    n += 1  # 修改形参的值
    print(f"修改后n={n}")
    return n  # 返回修改后的值

a = 10
print(f"调用前a={a}")  # a=10
result = test(a)  # 调用函数，a是实参
print(f"调用后a={a}")  # a仍然是10，说明实参没有被修改
print(f"函数返回值={result}")  # result接收函数的返回值 